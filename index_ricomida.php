        <div class="col">Masa</div>
        <div class="col"></div>
        <div class="col"></div>
        <hr>
        <div class="w-100"></div>
        <div class="col">
          <p class="mt-5">20 gr de levadura seca instantánea</p>
          <p>1 cucharadita de azúcar</p>
          <p>500 ml agua tibia</p>
          <p>100 ml aceite vegetal</p>
          <p>4 cucharadas de aceite de oliva</p>
          <p>65 gr harina de maíz fina (chuchoca)</p>
          <p>2 cucharaditas de sal</p>
          <p>625 gr harina</p>
          <p>Salsa</p>
          <p class="mt-5">5 cucharadas de aceite de oliva</p>
          <p>½ cebolla picada en cubos finos</p>
          <p>2 cucharaditas de sal</p>
          <p>3 cucharadas de Orégano Gourmet</p>
          <p>1 ½ cucharadas de Albahaca Gourmet</p>
          <p>2 tarro de tomates en cubos al natural</p>
          <p>2 cucharaditas de azúcar Relleno</p>
          <p></p>
          <p class="mt-5">1/2 cebolla , en cubos</p>
        </div>
        <div class="col"></div>
        <div class="col">
          <p class="mt-5">1. Para la masa, disolver la levadura y el azúcar en agua tibia en un bol grande. Dejar reposar 2 minutos. Agregar el aceite, la harina de maíz, sal y 400gr de harina. Mezclar bien. Amasar y agregar el resto de harina, la cantidad necesaria para tener una masa blanda. Poner en el mesón y amasar por 5 minutos o hasta tener una masa lisa y elástica (agregar harina en el mesón a medida que se va amasando para que la masa no se pegue) . Poner la masa en un bol aceitado, tapar con un paño de cocina limpio y dejar reposar hasta que la masa duplique su volumen (leudar). Golpear la masa para que baje y luego dejar leudar por 40 minutos adicionales.</p>
          <p class="mt-5"> 2. Mientras tanto preparar la salsa: Calentar 4 cucharadas de aceite en una olla a fuego medio. Agregar la cebolla, Orégano Gourmet, Albahaca Gourmet, sal y cocinar hasta que la cebolla esté dorada. Agregar el ajo y cocinar por 30 segundos. Agregar los tomates junto al jugo del tarro, a la mezcla de cebolla. Agregar el azúcar y cocinar hasta que la mezcla se haya reducido y espesado, como 30 minutos. Sazonar con sal y pimienta a gusto. Reservar.</p>
          <p class="mt-5">3. Relleno: Cocinar las chalotas en el aceite de oliva hasta que estén transparentes. Agregar los champiñones y las espinacas, cocinar hasta que estén cocidos y se haya evaporado todo el líquido que hayan votado los vegetales. Reservar.
            <p>4. Calentar el horno a 220C.</p>
            <p class="mt-5">5. Una vez que la masa haya reposado, dividirla en dos bolas. Estirar cada masa para cubrir dos moldes (desmontables) de 26 cm, hacer un borde de 5 cm con la masa. Dividir el relleno entre las dos pizza, tapar con el queso mozarella, luego terminar con la salsa de tomates y finalmente espolvorear el queso rallado.</p>
            <p class="mt-5">6. Hornear por 35 a 40 minutos o hasta que el relleno esté caliente y la base esté esponjosa y dorada. Esperar unos minutos antes de desmoldar.</p>
